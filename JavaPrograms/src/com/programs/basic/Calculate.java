package com.programs.basic;

public class Calculate {
	public static void main(String[] args) {
		int a = 30;
		int b = 15;
		int sum = a+b;
		System.out.println(sum);
		sum = a-b;
		System.out.println(sum);
		sum = a*b;
		System.out.println(sum);
		sum = a/b;
		System.out.println(sum);
		sum = a%b;
		System.out.println(sum);
	}

}
